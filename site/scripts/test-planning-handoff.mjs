export async function validateTestPlanningHandoffDist({ baseUrl = "https://tracemap.tools", dist, errors }) {
  const localErrors = [];
  const cleanBaseUrl = normalizeBaseUrl(baseUrl, localErrors);
  const pagePath = resolve(dist, "test-planning", "index.html");

  if (!(await fileExists(pagePath))) {
    localErrors.push(withEvidence("Test planning handoff page is missing required public route: /test-planning/", pageArtifact));
    errors.push(...localErrors);
    return;
  }

  if (cleanBaseUrl) {
    await validateSitemap({ baseUrl: cleanBaseUrl, dist, errors: localErrors });
  }

  const routeContext = await readRouteContext({ dist, errors: localErrors });
  await validateTestPlanningHandoffPage({ pagePath, routeContext, errors: localErrors });
  await validateInboundLinks({ dist, errors: localErrors });

  errors.push(...localErrors);
}

function normalizeBaseUrl(value, errors) {
  let url;

  try {
    url = new URL(value);
  } catch {
    errors.push(withEvidence(`Test planning handoff baseUrl must be a valid absolute URL: ${String(value)}`, "baseUrl input"));
    return null;
  }

  if (url.protocol !== "https:" && url.protocol !== "http:") {
    errors.push(withEvidence(`Test planning handoff baseUrl must use http or https: ${String(value)}`, "baseUrl input"));
    return null;
  }

  return url.origin;
}

async function validateSitemap({ baseUrl, dist, errors }) {
  const sitemapPath = resolve(dist, "sitemap.xml");
  if (!(await fileExists(sitemapPath))) {
    return;
  }

  const sitemapUrls = await readSitemapLocSet(sitemapPath);
  if (!sitemapUrls.has(`${baseUrl}${testPlanningHandoffRoute}`)) {
    errors.push(withEvidence(`Test planning handoff sitemap is missing required route: ${baseUrl}${testPlanningHandoffRoute}`, sitemapArtifact));
  }
}

async function readRouteContext({ dist, errors }) {
  const indexPath = resolve(dist, "routes-index.json");
  const sitemapPath = resolve(dist, "sitemap.xml");
  const routes = new Set();
  const sitemapRoutes = new Set();
  let routeEntry = null;

  if (await fileExists(sitemapPath)) {
    for (const loc of await readSitemapLocSet(sitemapPath)) {
      try {
        sitemapRoutes.add(normalizeRouteHref(new URL(loc).pathname));
      } catch {
        // The aggregate validator reports malformed sitemap URLs separately.
      }
    }
  }

  if (!(await fileExists(indexPath))) {
    return { routeEntry, routes, sitemapRoutes };
  }

  let parsed;
  try {
    parsed = JSON.parse(await readFile(indexPath, "utf8"));
  } catch (error) {
    errors.push(withEvidence(`Test planning handoff could not parse routes-index.json: ${error.message}`, routesIndexArtifact));
    return { routeEntry, routes, sitemapRoutes };
  }

  if (!parsed || typeof parsed !== "object" || Array.isArray(parsed) || !Array.isArray(parsed.entries)) {
    errors.push(withEvidence("Test planning handoff routes-index.json is invalid: expected entries array", routesIndexArtifact));
    return { routeEntry, routes, sitemapRoutes };
  }

  for (const entry of parsed.entries) {
    if (typeof entry?.path === "string") {
      routes.add(normalizeRouteHref(entry.path));
    }
  }

  routeEntry = parsed.entries.find((entry) => normalizeRouteHref(entry?.path ?? "") === testPlanningHandoffRoute) ?? null;
  validateRouteEntry(routeEntry, errors);

  return { routeEntry, routes, sitemapRoutes };
}

function validateRouteEntry(routeEntry, errors) {
  if (!routeEntry) {
    errors.push(withEvidence(`Test planning handoff routes-index.json is missing required route: ${testPlanningHandoffRoute}`, routesIndexArtifact));
    return;
  }

  const expectedFields = {
    publicClaimLevel: "concept",
    hintCategory: "use-case",
    sourceType: "site-page",
    preferredProofPath: "/proof-paths/"
  };

  for (const [field, expected] of Object.entries(expectedFields)) {
    if (routeEntry[field] !== expected) {
      errors.push(withEvidence(`Test planning handoff routes-index.json expected ${field} ${expected}, got ${String(routeEntry[field])}`, routesIndexArtifact));
    }
  }

  if (!Array.isArray(routeEntry.limitations) || routeEntry.limitations.length === 0) {
    errors.push(withEvidence("Test planning handoff routes-index.json must include limitations metadata.", routesIndexArtifact));
  }

  if (!Array.isArray(routeEntry.nonClaims) || routeEntry.nonClaims.length === 0) {
    errors.push(withEvidence("Test planning handoff routes-index.json must include nonClaims metadata.", routesIndexArtifact));
    return;
  }

  const normalizedNonClaims = normalizeScanText(routeEntry.nonClaims.join(" ")).toLowerCase();
  for (const term of metadataNonClaimTerms) {
    if (!normalizedNonClaims.includes(term.toLowerCase())) {
      errors.push(withEvidence(`Test planning handoff routes-index.json nonClaims are missing required term: ${term}`, routesIndexArtifact));
    }
  }

  const publicMetadataText = [routeEntry.title, routeEntry.summary, ...(routeEntry.limitations ?? [])].join(" ");
  validateForbiddenClaims({ errors, text: publicMetadataText, label: "metadata", artifact: routesIndexArtifact });
  validateBlameLanguage({ errors, text: publicMetadataText, label: "metadata", artifact: routesIndexArtifact });
}

async function validateTestPlanningHandoffPage({ pagePath, routeContext, errors }) {
  const html = await readFile(pagePath, "utf8");
  const decodedHtml = decodeHtmlEntities(html);
  const mainHtml = extractMainHtml(html);
  const strippedHtml = stripSanctionedBoundaryRegions(html);
  const strippedMainHtml = extractMainHtml(strippedHtml);
  const pageText = normalizeRenderedText(mainHtml);
  const lowerPageText = pageText.toLowerCase();
  const strippedText = normalizeRenderedText(strippedMainHtml);
  const strippedTextTight = normalizeTightHtmlText(strippedMainHtml);
  const metadataText = collectMetadataText(html);
  const attributeText = collectDecodedAttributeText(strippedHtml);
  const allAttributeText = collectDecodedAttributeText(html);
  const allTextTight = normalizeTightHtmlText(html);
  const wordCount = countWords(pageText);

  for (const phrase of requiredText) {
    if (!pageText.includes(phrase)) {
      errors.push(withEvidence(`Test planning handoff page is missing required text: ${phrase}`, pageArtifact));
    }
  }

  for (const section of requiredSections) {
    if (!pageText.includes(section)) {
      errors.push(withEvidence(`Test planning handoff page is missing required section: ${section}`, pageArtifact));
    }
  }

  for (const field of requiredFields) {
    if (!lowerPageText.includes(field.toLowerCase())) {
      errors.push(withEvidence(`Test planning handoff page is missing required field: ${field}`, pageArtifact));
    }
    if (!hasDataRow(html, "data-test-planning-field", field)) {
      errors.push(withEvidence(`Test planning handoff page is missing required field row: ${field}`, pageArtifact));
    }
  }

  for (const condition of requiredStopConditions) {
    if (!lowerPageText.includes(condition.toLowerCase())) {
      errors.push(withEvidence(`Test planning handoff page is missing stop condition: ${condition}`, pageArtifact));
    }
    if (!hasDataRow(html, "data-stop-condition", condition)) {
      errors.push(withEvidence(`Test planning handoff page is missing stop condition marker: ${condition}`, pageArtifact));
    }
  }

  for (const term of requiredCoverageTerms) {
    if (!lowerPageText.includes(term)) {
      errors.push(withEvidence(`Test planning handoff page is missing coverage caveat term: ${term}`, pageArtifact));
    }
  }

  for (const [route, distinction] of Object.entries(requiredNeighborDistinctions)) {
    if (!hasHref(html, route)) {
      errors.push(withEvidence(`Test planning handoff page is missing required link: ${route}`, pageArtifact));
    }
    if (!hasDataRow(html, "data-neighbor-route", route)) {
      errors.push(withEvidence(`Test planning handoff page is missing neighbor marker: ${route}`, pageArtifact));
    }
    if (!pageText.includes(distinction)) {
      errors.push(withEvidence(`Test planning handoff page is missing neighbor distinction for ${route}`, pageArtifact));
    }
  }

  for (const owner of requiredOwnerRoles) {
    if (!lowerPageText.includes(owner)) {
      errors.push(withEvidence(`Test planning handoff page is missing owner role: ${owner}`, pageArtifact));
    }
  }

  if ((html.match(/\bdata-safe-handoff-example\b/g) ?? []).length < 3) {
    errors.push(withEvidence("Test planning handoff page must include at least three safe handoff examples.", pageArtifact));
  }

  validatePageMetadata(html, errors);
  validateInternalRouteLinks(html, routeContext, errors);

  if (wordCount < 450 || wordCount > 1600) {
    errors.push(withEvidence(`Test planning handoff page word count must be between 450 and 1600 words, got ${wordCount}`, pageArtifact));
  }

  validateForbiddenClaims({
    errors,
    text: `${strippedText} ${strippedTextTight} ${metadataText} ${attributeText}`,
    label: "page copy outside sanctioned boundary regions"
  });
  validateRawMaterial({
    errors,
    text: `${normalizeRenderedText(strippedHtml)} ${normalizeTightHtmlText(strippedHtml)} ${metadataText} ${attributeText}`,
    label: "outside sanctioned boundary regions"
  });
  validateHardPrivateMaterial({
    errors,
    text: `${html} ${decodedHtml} ${pageText} ${allTextTight} ${metadataText} ${allAttributeText} ${JSON.stringify(routeContext.routeEntry ?? {})}`,
    label: "page, attributes, or metadata"
  });
  validateBlameLanguage({
    errors,
    text: `${pageText} ${metadataText} ${allAttributeText}`,
    label: "page, attributes, or metadata"
  });
}

function validatePageMetadata(html, errors) {
  const metaTags = findTagAttributes(html, "meta");
  const linkTags = findTagAttributes(html, "link");
  const checks = [
    [/<title>[^<]+<\/title>/i.test(html), "title"],
    [hasMeta(metaTags, { name: "description", content: "non-empty" }), "description"],
    [
      linkTags.some(
        (attributes) =>
          hasRel(attributes, "canonical") &&
          getAttribute(attributes, "href") === "https://tracemap.tools/test-planning/"
      ),
      "canonical URL"
    ],
    [hasMeta(metaTags, { property: "og:type", content: "article" }), "Open Graph type"],
    [hasMeta(metaTags, { property: "og:title", content: "non-empty" }), "Open Graph title"],
    [hasMeta(metaTags, { property: "og:description", content: "non-empty" }), "Open Graph description"],
    [
      hasMeta(metaTags, {
        property: "og:url",
        content: "https://tracemap.tools/test-planning/"
      }),
      "Open Graph URL"
    ]
  ];

  for (const [passed, label] of checks) {
    if (!passed) {
      errors.push(withEvidence(`Test planning handoff page is missing required metadata: ${label}`, pageArtifact));
    }
  }
}

function validateInternalRouteLinks(html, { routes, sitemapRoutes }, errors) {
  if (routes.size === 0 && sitemapRoutes.size === 0) {
    return;
  }

  for (const href of extractHrefs(extractMainHtml(html))) {
    if (!href.startsWith("/") || href.startsWith("//")) {
      continue;
    }

    const route = normalizeRouteHref(href);
    if (!routes.has(route) && !sitemapRoutes.has(route)) {
      errors.push(withEvidence(`Test planning handoff page links to unresolved internal route: ${route}`, pageArtifact));
    }
  }
}

async function validateInboundLinks({ dist, errors }) {
  const missing = [];

  for (const route of testPlanningHandoffInboundRoutes) {
    const pagePath = resolve(dist, route.replace(/^\/|\/$/g, ""), "index.html");
    if (!(await fileExists(pagePath))) {
      continue;
    }

    const html = await readFile(pagePath, "utf8");
    if (!hasHref(html, testPlanningHandoffRoute)) {
      missing.push(route);
    }
  }

  if (missing.length > 0) {
    errors.push(withEvidence(`Test planning handoff is missing inbound links from live adjacent routes: ${missing.join(", ")}`, "adjacent route HTML"));
  }
}

function validateForbiddenClaims({ artifact = pageArtifact, errors, text, label }) {
  for (const pattern of forbiddenClaimPatterns) {
    const flags = pattern.flags.includes("g") ? pattern.flags : `${pattern.flags}g`;
    const globalPattern = new RegExp(pattern.source, flags);
    for (const match of text.matchAll(globalPattern)) {
      if (!hasNegatedContext(text, match.index)) {
        errors.push(withEvidence(`Test planning handoff contains forbidden public claim in ${label}: ${match[0]}`, artifact));
        break;
      }
    }
  }
}

function validateRawMaterial({ errors, text, label }) {
  for (const pattern of rawMaterialPatterns) {
    if (pattern.test(text)) {
      errors.push(withEvidence(`Test planning handoff contains forbidden raw/private material ${label}: ${pattern.source}`, pageArtifact));
    }
  }
}

function validateHardPrivateMaterial({ errors, text, label }) {
  for (const pattern of hardPrivatePatterns) {
    if (pattern.test(text)) {
      errors.push(withEvidence(`Test planning handoff contains hard private material in ${label}: ${pattern.source}`, pageArtifact));
    }
  }
}

function validateBlameLanguage({ artifact = pageArtifact, errors, text, label }) {
  for (const pattern of blameLanguagePatterns) {
    if (pattern.test(text)) {
      errors.push(withEvidence(`Test planning handoff contains blame language in ${label}: ${pattern.source}`, artifact));
    }
  }
}

function hasNegatedContext(value, index) {
  const prefix = value.slice(Math.max(0, index - 64), index).toLowerCase();
  return /(?:not|no|without|cannot|can't|does not|do not|never)\s+(?:a\s+)?(?:complete\s+)?(?:new\s+)?$/.test(prefix);
}

function hasDataRow(html, attribute, value) {
  const escaped = escapeRegExp(value);
  return new RegExp(`\\b${attribute}\\s*=\\s*["']${escaped}["']`, "i").test(html);
}

function stripSanctionedBoundaryRegions(html) {
  return html.replace(sanctionedBoundarySectionPattern, " ");
}

function collectMetadataText(html) {
  const values = [];
  for (const attributes of findTagAttributes(html, "meta")) {
    const content = getAttribute(attributes, "content");
    if (content) {
      values.push(content);
    }
  }

  const title = html.match(/<title>([\s\S]*?)<\/title>/i);
  if (title) {
    values.push(title[1]);
  }

  return decodeHtmlEntities(values.join(" "));
}

function collectDecodedAttributeText(html) {
  return decodeHtmlEntities(
    findAllTagAttributes(html)
      .flatMap((attributes) => [...attributes.matchAll(/\s[a-z:-]+\s*=\s*("[^"]*"|'[^']*')/gi)])
      .map((match) => unquoteAttributeValue(match[1]))
      .join(" ")
  );
}

function normalizeTightHtmlText(html) {
  return normalizeScanText(decodeHtmlEntities(stripTagsTight(html)));
}

function normalizeScanText(value) {
  return String(value).replace(/\s+/g, " ").trim();
}

function extractMainHtml(html) {
  const match = html.match(/<main\b[^>]*>([\s\S]*?)<\/main>/i);
  return match ? match[1] : html;
}

function stripTagsTight(html) {
  let text = "";
  let insideTag = false;
  let quote = "";
  const value = String(html);

  for (let index = 0; index < value.length; index += 1) {
    const char = value[index];

    if (insideTag) {
      if (quote) {
        if (char === quote) {
          quote = "";
        }
        continue;
      }

      if (char === "\"" || char === "'") {
        quote = char;
        continue;
      }

      if (char === ">") {
        insideTag = false;
      }
      continue;
    }

    if (char === "<") {
      if (value.startsWith("<!--", index)) {
        const end = value.indexOf("-->", index + 4);
        index = end === -1 ? value.length : end + 2;
        continue;
      }

      insideTag = true;
      quote = "";
      continue;
    }

    text += char;
  }

  return text;
}

function countWords(text) {
  const words = text.match(/[A-Za-z0-9][A-Za-z0-9'/-]*/g);
  return words ? words.length : 0;
}

function findTagAttributes(html, tagName) {
  const pattern = new RegExp(`<${tagName}\\b([^>]*)>`, "gi");
  return [...html.matchAll(pattern)].map((match) => match[1]);
}

function findAllTagAttributes(html) {
  return [...String(html).matchAll(/<[a-z][a-z0-9:-]*\b([^>]*)>/gi)].map((match) => match[1]);
}

function hasMeta(metaTags, expected) {
  return metaTags.some((attributes) => {
    if (expected.name && getAttribute(attributes, "name") !== expected.name) {
      return false;
    }

    if (expected.property && getAttribute(attributes, "property") !== expected.property) {
      return false;
    }

    const content = getAttribute(attributes, "content");
    return expected.content === "non-empty" ? Boolean(content?.trim()) : content === expected.content;
  });
}

function hasRel(attributes, expectedRel) {
  return (getAttribute(attributes, "rel") ?? "")
    .split(/\s+/)
    .some((rel) => rel.toLowerCase() === expectedRel.toLowerCase());
}

function getAttribute(attributes, name) {
  const escaped = escapeRegExp(name);
  const match = String(attributes).match(new RegExp(`\\s${escaped}\\s*=\\s*("[^"]*"|'[^']*'|[^\\s>]+)`, "i"));
  return match ? unquoteAttributeValue(match[1]) : null;
}

function unquoteAttributeValue(value) {
  return String(value).replace(/^["']|["']$/g, "");
}

function hasHref(html, expectedHref) {
  const normalizedExpected = normalizeRouteHref(expectedHref);
  return extractHrefs(html).some((href) => normalizeRouteHref(href) === normalizedExpected);
}

function extractHrefs(html) {
  return [...String(html).matchAll(/\shref\s*=\s*("[^"]*"|'[^']*'|[^\s>]+)/gi)].map((match) =>
    unquoteAttributeValue(match[1])
  );
}

function normalizeRouteHref(href) {
  const value = String(href).trim();
  if (!value.startsWith("/") || value.startsWith("//")) {
    return value;
  }

  const withoutHash = value.split("#")[0].split("?")[0];
  if (withoutHash === "") {
    return "/";
  }

  return withoutHash.endsWith("/") ? withoutHash : `${withoutHash}/`;
}

function withEvidence(message, artifact) {
  return `${message} [${artifact}]`;
}
