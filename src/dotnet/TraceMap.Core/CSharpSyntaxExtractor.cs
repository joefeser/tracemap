    private static void AddParseDiagnostics(ScanManifest manifest, List<CodeFact> facts, SyntaxTree tree)
    {
        foreach (var diagnostic in tree.GetDiagnostics()
            .Where(diagnostic => diagnostic.Severity == DiagnosticSeverity.Error)
            .OrderBy(diagnostic => diagnostic.Location.GetLineSpan().StartLinePosition.Line)
            .ThenBy(diagnostic => diagnostic.Id, StringComparer.Ordinal))
        {
            var span = diagnostic.Location.GetLineSpan();
            var filePath = FileInventory.NormalizeRelativePath(span.Path);
            facts.Add(FactFactory.Create(
                manifest,
                FactTypes.AnalysisGap,
                RuleIds.CSharpSyntaxDeclarations,
                EvidenceTiers.Tier4Unknown,
                new EvidenceSpan(
                    filePath,
                    span.StartLinePosition.Line + 1,
                    Math.Max(span.StartLinePosition.Line + 1, span.EndLinePosition.Line + 1),
                    null,
                    "CSharpSyntaxExtractor",
                    ScannerVersions.CSharpSyntaxExtractor),
                properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                {
                    ["diagnosticId"] = diagnostic.Id,
                    ["message"] = diagnostic.GetMessage()
                }));
        }
    }

