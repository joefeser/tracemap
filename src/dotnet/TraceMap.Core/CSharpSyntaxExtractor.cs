using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace TraceMap.Core;

public static class CSharpSyntaxExtractor
{
    public static IReadOnlyList<CodeFact> Extract(string repoPath, ScanManifest manifest, IEnumerable<FileInventoryItem> inventory)
    {
        var facts = new List<CodeFact>();
        foreach (var file in inventory
            .Where(item => FileInventory.IsCSharpKind(item.Kind))
            .OrderBy(item => item.RelativePath, StringComparer.Ordinal))
        {
            var fullPath = Path.Combine(repoPath, file.RelativePath);
            if (TryGetBoilerplateCategory(file.RelativePath, out var boilerplateCategory))
            {
                facts.Add(CreateFileShapeFact(manifest, file.RelativePath, FactTypes.InfrastructureBoilerplate, boilerplateCategory));
            }

            if (IsGeneratedSource(fullPath))
            {
                continue;
            }

            try
            {
                var source = File.ReadAllText(fullPath);
                if (HasAutoGeneratedHeader(source))
                {
                    continue;
                }

                var tree = CSharpSyntaxTree.ParseText(SourceText.From(source), path: file.RelativePath);
                var root = tree.GetCompilationUnitRoot();
                AddParseDiagnostics(manifest, facts, tree);
                AddDeclarationFacts(manifest, facts, file.RelativePath, root);
                AddAttributeFacts(manifest, facts, file.RelativePath, root);
                AddMemberAccessFacts(manifest, facts, file.RelativePath, root);
                AddInvocationFacts(manifest, facts, file.RelativePath, root);
                AddObjectCreationFacts(manifest, facts, file.RelativePath, root);
                AddLogicShapeFacts(manifest, facts, file.RelativePath, root);
                AddQueryPatternFacts(manifest, facts, file.RelativePath, root);
                AddObjectShapeFacts(manifest, facts, file.RelativePath, root);
                AddAspNetRouteFacts(manifest, facts, file.RelativePath, root);
                AddRazorModelBindingTargetFacts(manifest, facts, file.RelativePath, root);
            }
            catch (Exception ex) when (ex is IOException or UnauthorizedAccessException)
            {
                facts.Add(FactFactory.Create(
                    manifest,
                    FactTypes.AnalysisGap,
                    RuleIds.CSharpSyntaxDeclarations,
                    EvidenceTiers.Tier4Unknown,
                    new EvidenceSpan(file.RelativePath, 1, 1, null, "CSharpSyntaxExtractor", ScannerVersions.CSharpSyntaxExtractor),
                    properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                    {
                        ["message"] = $"Unable to read C# file for syntax extraction: {ex.Message}"
                    }));
            }
        }

        return facts;
    }

    private static void AddDeclarationFacts(ScanManifest manifest, List<CodeFact> facts, string filePath, CompilationUnitSyntax root)
    {
        foreach (var node in root.DescendantNodes().OfType<BaseTypeDeclarationSyntax>())
        {
            switch (node)
            {
                case EnumDeclarationSyntax enumDeclaration:
                    facts.Add(CreateSyntaxFact(
                        manifest,
                        FactTypes.EnumDeclared,
                        RuleIds.CSharpSyntaxDeclarations,
                        filePath,
                        enumDeclaration,
                        targetSymbol: enumDeclaration.Identifier.ValueText,
                        properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                        {
                            ["name"] = enumDeclaration.Identifier.ValueText,
                            ["kind"] = "enum"
                        }));
                    break;
                case TypeDeclarationSyntax typeDeclaration:
                    var typeName = typeDeclaration.Identifier.ValueText;
                    facts.Add(CreateSyntaxFact(
                        manifest,
                        FactTypes.TypeDeclared,
                        RuleIds.CSharpSyntaxDeclarations,
                        filePath,
                        typeDeclaration,
                        targetSymbol: typeName,
                        properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                        {
                            ["name"] = typeName,
                            ["kind"] = typeDeclaration.Keyword.ValueText
                        }));
                    break;
            }
        }

        foreach (var method in root.DescendantNodes().OfType<MethodDeclarationSyntax>())
        {
            facts.Add(CreateSyntaxFact(
                manifest,
                FactTypes.MethodDeclared,
                RuleIds.CSharpSyntaxDeclarations,
                filePath,
                method,
                targetSymbol: method.Identifier.ValueText,
                properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                {
                    ["name"] = method.Identifier.ValueText
                }));
        }

        foreach (var property in root.DescendantNodes().OfType<PropertyDeclarationSyntax>())
        {
            var containingType = property.Ancestors().OfType<TypeDeclarationSyntax>().FirstOrDefault()?.Identifier.ValueText ?? string.Empty;
            var propertyName = property.Identifier.ValueText;
            facts.Add(CreateSyntaxFact(
                manifest,
                FactTypes.PropertyDeclared,
                RuleIds.CSharpSyntaxDeclarations,
                filePath,
                property,
                sourceSymbol: containingType,
                targetSymbol: string.IsNullOrWhiteSpace(containingType) ? propertyName : $"{containingType}.{propertyName}",
                contractElement: propertyName,
                properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                {
                    ["containingType"] = containingType,
                    ["declaredType"] = property.Type.ToString(),
                    ["modelKind"] = ModelKindForType(containingType),
                    ["name"] = propertyName,
                    ["propertyName"] = propertyName
                }));
        }
    }

    private static void AddAttributeFacts(ScanManifest manifest, List<CodeFact> facts, string filePath, CompilationUnitSyntax root)
    {
        foreach (var attribute in root.DescendantNodes().OfType<AttributeSyntax>())
        {
            var attributeName = attribute.Name.ToString();
            facts.Add(CreateSyntaxFact(
                manifest,
                FactTypes.AttributeUsed,
                RuleIds.CSharpSyntaxDeclarations,
                filePath,
                attribute,
                targetSymbol: attributeName,
                properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                {
                    ["name"] = attributeName
                }));
        }
    }

    private static void AddMemberAccessFacts(ScanManifest manifest, List<CodeFact> facts, string filePath, CompilationUnitSyntax root)
    {
        foreach (var memberAccess in root.DescendantNodes().OfType<MemberAccessExpressionSyntax>())
        {
            var memberName = memberAccess.Name.Identifier.ValueText;
            if (string.IsNullOrWhiteSpace(memberName))
            {
                memberName = memberAccess.Name.ToString();
            }

            facts.Add(CreateSyntaxFact(
                manifest,
                FactTypes.MemberAccessName,
                RuleIds.CSharpSyntaxMemberAccess,
                filePath,
                memberAccess,
                sourceSymbol: GetSafeExpressionName(memberAccess.Expression),
                targetSymbol: memberName,
                properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                {
                    ["expressionHash"] = FactFactory.Hash(memberAccess.Expression.ToString(), 32),
                    ["expressionKind"] = memberAccess.Expression.Kind().ToString(),
                    ["memberName"] = memberName
                }));
        }
    }

    private static void AddInvocationFacts(ScanManifest manifest, List<CodeFact> facts, string filePath, CompilationUnitSyntax root)
    {
        foreach (var invocation in root.DescendantNodes().OfType<InvocationExpressionSyntax>())
        {
            var invocationName = GetInvocationName(invocation.Expression);
            var containingMember = GetContainingMemberName(invocation);
            var receiverName = GetInvocationReceiverName(invocation.Expression);
            facts.Add(CreateSyntaxFact(
                manifest,
                FactTypes.InvocationName,
                RuleIds.CSharpSyntaxInvocation,
                filePath,
                invocation,
                targetSymbol: invocationName,
                properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                {
                    ["expressionHash"] = FactFactory.Hash(invocation.Expression.ToString(), 32),
                    ["expressionKind"] = invocation.Expression.Kind().ToString(),
                    ["invocationName"] = invocationName,
                    ["receiverName"] = receiverName ?? string.Empty
                }));

            facts.Add(CreateSyntaxFact(
                manifest,
                FactTypes.CallEdge,
                RuleIds.CSharpSyntaxCallGraph,
                filePath,
                invocation,
                sourceSymbol: containingMember,
                targetSymbol: invocationName,
                properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                {
                    ["callerName"] = containingMember ?? string.Empty,
                    ["calleeName"] = invocationName,
                    ["callKind"] = "SyntaxInvocation"
                }));

            if (IsSerializationName(invocationName) || (receiverName?.Contains("Serializer", StringComparison.Ordinal) ?? false))
            {
                facts.Add(CreateSyntaxFact(
                    manifest,
                    FactTypes.SerializationLogic,
                    RuleIds.CSharpSyntaxLogicShape,
                    filePath,
                    invocation,
                    sourceSymbol: containingMember,
                    targetSymbol: invocationName,
                    properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                    {
                        ["callerName"] = containingMember ?? string.Empty,
                        ["operationName"] = invocationName
                    }));
            }
        }
    }

    private static void AddObjectCreationFacts(ScanManifest manifest, List<CodeFact> facts, string filePath, CompilationUnitSyntax root)
    {
        foreach (var creation in root.DescendantNodes().OfType<ObjectCreationExpressionSyntax>())
        {
            var typeName = creation.Type.ToString();
            var containingMember = GetContainingMemberName(creation);
            var assignedTo = GetAssignedVariableName(creation);
            facts.Add(CreateSyntaxFact(
                manifest,
                FactTypes.ObjectCreated,
                RuleIds.CSharpSyntaxObjectCreation,
                filePath,
                creation,
                sourceSymbol: containingMember,
                targetSymbol: typeName,
                contractElement: LastTypePart(typeName),
                properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                {
                    ["callerName"] = containingMember ?? string.Empty,
                    ["createdType"] = typeName,
                    ["assignedTo"] = assignedTo ?? string.Empty,
                    ["argumentCount"] = (creation.ArgumentList?.Arguments.Count ?? 0).ToString(),
                    ["creationKind"] = "SyntaxObjectCreation"
                }));

            facts.Add(CreateSyntaxFact(
                manifest,
                FactTypes.CallEdge,
                RuleIds.CSharpSyntaxCallGraph,
                filePath,
                creation,
                sourceSymbol: containingMember,
                targetSymbol: typeName,
                properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                {
                    ["callerName"] = containingMember ?? string.Empty,
                    ["calleeName"] = typeName,
                    ["calleeContainingType"] = typeName,
                    ["callKind"] = "SyntaxObjectCreation",
                    ["assignedTo"] = assignedTo ?? string.Empty
                }));
        }
    }

    private static void AddLogicShapeFacts(ScanManifest manifest, List<CodeFact> facts, string filePath, CompilationUnitSyntax root)
    {
        foreach (var binary in root.DescendantNodes().OfType<BinaryExpressionSyntax>().Where(IsCalculationExpression))
        {
            facts.Add(CreateSyntaxFact(
                manifest,
                FactTypes.CalculationExpression,
                RuleIds.CSharpSyntaxLogicShape,
                filePath,
                binary,
                sourceSymbol: GetContainingMemberName(binary),
                targetSymbol: "calculation",
                properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                {
                    ["containingMember"] = GetContainingMemberName(binary) ?? string.Empty,
                    ["operator"] = binary.OperatorToken.ValueText,
                    ["expressionHash"] = FactFactory.Hash(binary.ToString(), 32)
                }));
        }

        foreach (var branch in root.DescendantNodes().Where(IsBranchingNode))
        {
            facts.Add(CreateSyntaxFact(
                manifest,
                FactTypes.BranchingLogic,
                RuleIds.CSharpSyntaxLogicShape,
                filePath,
                branch,
                sourceSymbol: GetContainingMemberName(branch),
                targetSymbol: branch.Kind().ToString(),
                properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                {
                    ["branchKind"] = branch.Kind().ToString(),
                    ["containingMember"] = GetContainingMemberName(branch) ?? string.Empty
                }));
        }

        foreach (var method in root.DescendantNodes().OfType<MethodDeclarationSyntax>().Where(method => LooksLikeRetryPolicyLogic(filePath, method)))
        {
            facts.Add(CreateSyntaxFact(
                manifest,
                FactTypes.RetryPolicyLogic,
                RuleIds.CSharpSyntaxLogicShape,
                filePath,
                method,
                sourceSymbol: method.Identifier.ValueText,
                targetSymbol: method.Identifier.ValueText,
                properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                {
                    ["methodName"] = method.Identifier.ValueText,
                    ["className"] = method.Ancestors().OfType<TypeDeclarationSyntax>().FirstOrDefault()?.Identifier.ValueText ?? string.Empty,
                    ["matchedBy"] = "RetryBackoffNamingOrTimeSpan"
                }));
        }
    }

    private static void AddQueryPatternFacts(ScanManifest manifest, List<CodeFact> facts, string filePath, CompilationUnitSyntax root)
    {
        foreach (var invocation in root.DescendantNodes().OfType<InvocationExpressionSyntax>())
        {
            if (invocation.Expression is not MemberAccessExpressionSyntax memberAccess)
            {
                continue;
            }

            var operationName = memberAccess.Name.Identifier.ValueText;
            if (!IsQueryOperation(operationName))
            {
                continue;
            }

            var fields = ExtractMemberNames(invocation.ArgumentList.Arguments.Select(argument => argument.Expression));
            var receiverName = GetSafeExpressionName(memberAccess.Expression) ?? string.Empty;
            facts.Add(FactFactory.Create(
                manifest,
                FactTypes.QueryPatternDetected,
                RuleIds.CSharpSyntaxQueryPattern,
                EvidenceTiers.Tier3SyntaxOrTextual,
                ToEvidenceSpan(filePath, invocation),
                sourceSymbol: GetContainingMemberName(invocation),
                targetSymbol: operationName,
                contractElement: operationName,
                properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                {
                    ["operationName"] = operationName,
                    ["receiverName"] = receiverName,
                    ["filterFields"] = IsFilterOperation(operationName) ? string.Join(";", fields) : string.Empty,
                    ["sortFields"] = IsSortOperation(operationName) ? string.Join(";", fields) : string.Empty,
                    ["selectFields"] = operationName == "Select" ? string.Join(";", fields) : string.Empty,
                    ["includeFields"] = operationName == "Include" ? string.Join(";", fields) : string.Empty,
                    ["fieldCount"] = fields.Count.ToString(),
                    ["patternHash"] = FactFactory.Hash(invocation.ToString(), 32)
                }));
        }
    }

    private static void AddObjectShapeFacts(ScanManifest manifest, List<CodeFact> facts, string filePath, CompilationUnitSyntax root)
    {
        foreach (var creation in root.DescendantNodes().OfType<ObjectCreationExpressionSyntax>())
        {
            if (creation.Initializer is null)
            {
                continue;
            }

            var fields = ExtractInitializerFields(creation.Initializer);
            if (fields.Count == 0)
            {
                continue;
            }

            facts.Add(CreateObjectShapeFact(manifest, filePath, creation, creation.Type.ToString(), fields));
        }

        foreach (var anonymous in root.DescendantNodes().OfType<AnonymousObjectCreationExpressionSyntax>())
        {
            var fields = anonymous.Initializers
                .Select(initializer => initializer.NameEquals?.Name.Identifier.ValueText
                    ?? initializer.Expression switch
                    {
                        IdentifierNameSyntax identifier => identifier.Identifier.ValueText,
                        MemberAccessExpressionSyntax memberAccess => memberAccess.Name.Identifier.ValueText,
                        _ => null
                    })
                .Where(name => !string.IsNullOrWhiteSpace(name))
                .Select(name => name!)
                .Distinct(StringComparer.Ordinal)
                .OrderBy(name => name, StringComparer.Ordinal)
                .ToArray();
            if (fields.Length == 0)
            {
                continue;
            }

            facts.Add(CreateObjectShapeFact(manifest, filePath, anonymous, "anonymous", fields));
        }
    }

    private static void AddAspNetRouteFacts(ScanManifest manifest, List<CodeFact> facts, string filePath, CompilationUnitSyntax root)
    {
        foreach (var type in root.DescendantNodes().OfType<ClassDeclarationSyntax>().OrderBy(type => type.Identifier.ValueText, StringComparer.Ordinal))
        {
            var controllerName = GetControllerName(type);
            if (!LooksLikeController(type, controllerName))
            {
                continue;
            }

            var controllerRoutes = AttributeRoutes(type.AttributeLists, "Route").DefaultIfEmpty(string.Empty).ToArray();
            var controllerTokens = new SortedDictionary<string, string>(StringComparer.OrdinalIgnoreCase)
            {
                ["controller"] = controllerName,
                ["action"] = string.Empty
            };

            foreach (var method in type.Members.OfType<MethodDeclarationSyntax>().OrderBy(method => method.Identifier.ValueText, StringComparer.Ordinal))
            {
                var httpAttributes = HttpMethodAttributes(method.AttributeLists).ToArray();
                var methodRoutes = AttributeRoutes(method.AttributeLists, "Route").ToArray();
                if (httpAttributes.Length == 0 && methodRoutes.Length == 0)
                {
                    continue;
                }

                if (httpAttributes.Length == 0)
                {
                    httpAttributes = [new HttpAttribute("ANY", string.Empty)];
                }

                var methodTokens = new SortedDictionary<string, string>(controllerTokens, StringComparer.OrdinalIgnoreCase)
                {
                    ["action"] = method.Identifier.ValueText
                };
                var bodyParameters = LikelyBodyParameters(method).ToArray();
                foreach (var httpAttribute in httpAttributes)
                {
                    var routes = new List<string>();
                    if (!string.IsNullOrWhiteSpace(httpAttribute.Template))
                    {
                        routes.Add(httpAttribute.Template);
                    }
                    routes.AddRange(methodRoutes);
                    if (routes.Count == 0)
                    {
                        routes.Add(string.Empty);
                    }

                    foreach (var route in routes)
                    {
                        foreach (var controllerRoute in controllerRoutes)
                        {
                            var combinedRoute = CombineRouteTemplates(controllerRoute, route);
                            var normalized = EndpointRouteNormalizer.Normalize(combinedRoute, tokens: methodTokens);
                            facts.Add(FactFactory.Create(
                                manifest,
                                FactTypes.HttpRouteBinding,
                                RuleIds.CSharpSyntaxAspNetRoute,
                                EvidenceTiers.Tier3SyntaxOrTextual,
                                new EvidenceSpan(
                                    filePath,
                                    method.SyntaxTree.GetLineSpan(method.Span).StartLinePosition.Line + 1,
                                    Math.Max(
                                        method.SyntaxTree.GetLineSpan(method.Span).StartLinePosition.Line + 1,
                                        method.SyntaxTree.GetLineSpan(method.Span).EndLinePosition.Line + 1),
                                    null,
                                    "CSharpAspNetSyntaxRouteExtractor",
                                    ScannerVersions.CSharpAspNetSyntaxRouteExtractor),
                                sourceSymbol: $"{controllerName}Controller.{method.Identifier.ValueText}",
                                targetSymbol: $"{httpAttribute.Method} {normalized.PathTemplate}",
                                contractElement: normalized.PathTemplate,
                                properties: new SortedDictionary<string, string>(StringComparer.Ordinal)
                                {
                                    ["methodName"] = httpAttribute.Method,
                                    ["httpMethods"] = httpAttribute.Method,
                                    ["routeTemplates"] = string.Join(",", new[] { controllerRoute, route }.Where(value => !string.IsNullOrWhiteSpace(value))),
                                    ["normalizedPathTemplate"] = normalized.PathTemplate,
                                    ["normalizedPathKey"] = normalized.PathKey,
                                    ["routeParameterNames"] = string.Join(";", normalized.ParameterNames),
                                    ["optionalParameterNames"] = string.Join(";", normalized.OptionalParameterNames),
                                    ["routeConstraints"] = string.Join(";", normalized.RouteConstraints),
                                    ["controllerName"] = controllerName,
                                    ["actionName"] = method.Identifier.ValueText,
                                    ["bodyParameterNames"] = string.Join(";", bodyParameters.Select(parameter => parameter.Name)),
                                    ["bodyParameterTypes"] = string.Join(";", bodyParameters.Select(parameter => parameter.Type)),
                                    ["authorizationKind"] = AuthorizationKind(type, method),
                                    ["staticMatchQuality"] = normalized.StaticMatchQuality
                                }));
                        }
                    }
                }
            }
        }
    }

    private static void AddRazorModelBindingTargetFacts(ScanManifest manifest, List<CodeFact> facts, string filePath, CompilationUnitSyntax root)
    {
        var propertyMap = root.DescendantNodes()
            .OfType<ClassDeclarationSyntax>()
            .ToDictionary(
                type => type.Identifier.ValueText,
                type => type.Members
                    .OfType<PropertyDeclarationSyntax>()
                    .Where(property => !string.IsNullOrWhiteSpace(property.Identifier.ValueText))
                    .Select(property => new ModelProperty(property.Identifier.ValueText, property.Type.ToString(), property))
                    .OrderBy(property => property.Name, StringComparer.Ordinal)
                    .ToArray(),
                StringComparer.Ordinal);

        foreach (var type in root.DescendantNodes().OfType<ClassDeclarationSyntax>().OrderBy(type => type.Identifier.ValueText, StringComparer.Ordinal))
        {
            var controllerName = GetControllerName(type);
            var isController = LooksLikeController(type, controllerName);
            var isPageModel = LooksLikePageModel(type, filePath);

            if (isController)
            {
                foreach (var method in type.Members.OfType<MethodDeclarationSyntax>().OrderBy(method => method.Identifier.ValueText, StringComparer.Ordinal))
                {
                    var httpMethods = HttpMethodAttributes(method.AttributeLists)
                        .Select(attribute => attribute.Method)
                        .Concat(AttributeRoutes(method.AttributeLists, "Route").Any() ? ["ANY"] : [])
                        .Distinct(StringComparer.Ordinal)
                        .OrderBy(value => value, StringComparer.Ordinal)
                        .ToArray();
                    if (httpMethods.Length == 0)
                    {
                        continue;
                    }

                    foreach (var parameter in method.ParameterList.Parameters.OrderBy(parameter => parameter.Identifier.ValueText, StringComparer.Ordinal))
                    {
                        AddParameterBindingTargets(
                            manifest,
                            facts,
                            filePath,
                            parameter,
                            "action-parameter",
                            controllerName,
                            method.Identifier.ValueText,
                            null,
                            httpMethods,
                            propertyMap);
                    }
                }
            }

            if (isPageModel)
            {
                foreach (var method in type.Members.OfType<MethodDeclarationSyntax>()
                    .Where(method => method.Identifier.ValueText.StartsWith("On", StringComparison.Ordinal))
                    .OrderBy(method => method.Identifier.ValueText, StringComparer.Ordinal))
                {
                    foreach (var parameter in method.ParameterList.Parameters.OrderBy(parameter => parameter.Identifier.ValueText, StringComparer.Ordinal))
                    {
                        AddParameterBindingTargets(
                            manifest,
                            facts,
                            filePath,
                            parameter,
                            "handler-parameter",
                            null,
                            null,
                            method.Identifier.ValueText,
                            [PageHandlerHttpMethod(method.Identifier.ValueText)],
                            propertyMap);
                    }
                }
            }

            foreach (var property in type.Members.OfType<PropertyDeclarationSyntax>().OrderBy(property => property.Identifier.ValueText, StringComparer.Ordinal))
            {
                if (HasAttribute(property.AttributeLists, "BindProperty"))
                {
                    AddModelBindingFact(
                        manifest,
                        facts,
                        filePath,
                        property,
                        "bind-property",
                        "view-model",
                        type.Identifier.ValueText,
                        property.Identifier.ValueText,
                        property.Identifier.ValueText,
                        property.Type.ToString(),
                        null,
                        "form",
                        null,
                        null,
                        null,
                        "POST");
                }
                else if (isPageModel)
                {
                    AddModelBindingFact(
                        manifest,
                        facts,
                        filePath,
                        property,
                        "page-model",
                        "view-model",
                        type.Identifier.ValueText,
                        property.Identifier.ValueText,
                        property.Identifier.ValueText,
                        property.Type.ToString(),
                        null,
                        "unknown",
                        null,
                        null,
                        null,
                        string.Empty);
                }
            }

            if (LooksLikeViewModelType(type.Identifier.ValueText))
            {
                foreach (var property in type.Members.OfType<PropertyDeclarationSyntax>().OrderBy(property => property.Identifier.ValueText, StringComparer.Ordinal))
                {
                    AddModelBindingFact(
                        manifest,
                        facts,
                        filePath,
                        property,
                        "view-model",
                        "view-model",
                        type.Identifier.ValueText,
                        property.Identifier.ValueText,
                        property.Identifier.ValueText,
                        property.Type.ToString(),
                        null,
                        "unknown",
                        null,
                        null,
                        null,
                        string.Empty);
                }
            }
        }
    }

    private static void AddParameterBindingTargets(
        ScanManifest manifest,
        List<CodeFact> facts,
        string filePath,
        ParameterSyntax parameter,
        string bindingKind,
        string? controllerName,
        string? actionName,
        string? handlerName,
        IReadOnlyList<string> httpMethods,
        IReadOnlyDictionary<string, ModelProperty[]> propertyMap)
    {
        var parameterType = CleanTypeName(parameter.Type?.ToString() ?? string.Empty);
        if (!propertyMap.TryGetValue(LastTypePart(parameterType), out var properties) || properties.Length == 0)
        {
            if (IsLikelyModelBindingParameterType(parameterType))
            {
                AddModelBindingGap(
                    manifest,
                    facts,
                    filePath,
                    parameter,
                    "cross-file-parameter-type",
                    bindingKind,
                    LastTypePart(parameterType),
                    parameter.Identifier.ValueText,
                    ParameterSource(parameter),
                    controllerName,
                    actionName,
                    handlerName,
                    httpMethods);
            }

            return;
        }

        var parameterSource = ParameterSource(parameter);
        var modelKind = parameterSource == "body" || LooksLikeDtoType(parameterType)
            ? "dto"
            : parameterSource == "form" ? "view-model" : "model";
        foreach (var httpMethod in httpMethods)
        {
            foreach (var property in properties)
            {
                AddModelBindingFact(
                    manifest,
                    facts,
                    filePath,
                    parameter,
                    bindingKind,
                    modelKind,
                    LastTypePart(parameterType),
                    property.Name,
                    property.Name,
                    property.TypeName,
                    parameter.Identifier.ValueText,
                    parameterSource,
                    controllerName,
                    actionName,
                    handlerName,
                    httpMethod);
            }
        }
    }

    private static void AddModelBindingFact(
        ScanManifest manifest,
        List<CodeFact> facts,
        string filePath,
        SyntaxNode node,
        string bindingKind,
        string modelKind,
        string modelType,
        string propertyName,
        string propertyPath,
        string propertyType,
        string? parameterName,
        string parameterSource,
        string? controllerName,
        string? actionName,
        string? handlerName,
        string httpMethod)
    {
        var properties = new SortedDictionary<string, string>(StringComparer.Ordinal)
        {
            ["bindingKind"] = bindingKind,
            ["httpMethod"] = httpMethod,
            ["modelKind"] = modelKind,
            ["modelType"] = modelType,
            ["parameterName"] = parameterName ?? string.Empty,
            ["parameterSource"] = parameterSource,
            ["propertyName"] = propertyName,
            ["propertyPath"] = propertyPath,
            ["propertyType"] = propertyType,
            ["uiFramework"] = "razor",
            ["valueStored"] = "safe-metadata-only"
        };
        if (!string.IsNullOrWhiteSpace(actionName))
        {
            properties["actionName"] = actionName!;
        }
        if (!string.IsNullOrWhiteSpace(controllerName))
        {
            properties["controllerName"] = controllerName!;
        }
        if (!string.IsNullOrWhiteSpace(handlerName))
        {
            properties["handlerName"] = handlerName!;
        }

        facts.Add(FactFactory.Create(
            manifest,
            FactTypes.RazorModelBindingTarget,
            RuleIds.RazorModelBinding,
            EvidenceTiers.Tier3SyntaxOrTextual,
            ToEvidenceSpan(filePath, node),
            sourceSymbol: actionName ?? handlerName ?? modelType,
            targetSymbol: $"{modelType}.{propertyName}",
            contractElement: propertyName,
            properties: properties));
    }

